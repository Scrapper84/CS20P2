package SkillBuilders;

public class evens
{


	public static void main(String[] args) 
	{
		final int maxvalue = 50;
		
		int newvalue = 0;
		
		System.out.println("even numbers between 1-20");
		
		while( newvalue < maxvalue)
		{
			newvalue += 2;
			System.out.println(newvalue);
		}
		
	}

}


